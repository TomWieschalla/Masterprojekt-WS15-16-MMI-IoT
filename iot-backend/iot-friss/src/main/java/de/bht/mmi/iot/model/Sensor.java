package de.bht.mmi.iot.model;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBMarshalling;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import de.bht.mmi.iot.converter.JodaDateTimeMarshaller;
import org.joda.time.DateTime;

import java.util.ArrayList;

@DynamoDBTable(tableName = "Sensor")
public class Sensor {

    private String sensorID;

    private boolean isActive;

    private DateTime creationDate;

    private String location;

    private String clusterID;

    private String ownerID;

    private int sensorType;

    private ArrayList<String> userList;


    public Sensor(boolean isActive, String location, String ownerID, int sensorType) {
        this.isActive = isActive;
        this.location = location;
        this.ownerID = ownerID;
        this.sensorType = sensorType;
    }

    public Sensor(boolean isActive, String location, String clusterID, String ownerID, int sensorType) {
        this.isActive = isActive;
        this.location = location;
        this.clusterID = clusterID;
        this.ownerID = ownerID;
        this.sensorType = sensorType;
    }

    public Sensor() {
    }

    @DynamoDBHashKey(attributeName = "sensorID")
    @DynamoDBAutoGeneratedKey
    public String getSensorID() {
        return sensorID;
    }

    public void setSensorID(String sensorID) {
        this.sensorID = sensorID;
    }

    public boolean isActive() {
        return isActive;
    }

    public void setActive(boolean active) {
        isActive = active;
    }

    @DynamoDBMarshalling(marshallerClass = JodaDateTimeMarshaller.class)
    public DateTime getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(DateTime creationDate) {
        this.creationDate = creationDate;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getClusterID() {
        return clusterID;
    }

    public void setClusterID(String clusterID) {
        this.clusterID = clusterID;
    }

    public String getOwnerID() {
        return ownerID;
    }

    public void setOwnerID(String ownerID) {
        this.ownerID = ownerID;
    }

    public int getSensorType() {
        return sensorType;
    }

    public void setSensorType(int sensorType) {
        this.sensorType = sensorType;
    }

    public ArrayList<String> getUserList() {
        return userList;
    }

    public void setUserList(ArrayList<String> userList) {
        this.userList = userList;
    }
}
